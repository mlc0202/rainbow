package com.icitic.ant.rdm;

public class Template {
	
	/**
	 * 模版名
	 */
	private String name;
	
	/**
	 * 转换后的文件后缀名
	 */
	private String suffix;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSuffix() {
		return suffix;
	}

	public void setSuffix(String suffix) {
		this.suffix = suffix;
	}
	
}
